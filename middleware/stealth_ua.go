// RandomUserAgent returns a cryptographically random user-agent string from the pool.
func RandomUserAgent() string {
	n, err := rand.Int(rand.Reader, big.NewInt(int64(len(userAgents))))
	if err != nil {
		return userAgents[0]
	}
	return userAgents[n.Int64()]
}

// UserAgentRotation returns middleware that sets a random Chrome user-agent per page.
func UserAgentRotation() browse.HandlerFunc {
	return func(c *browse.Context) {
		page := c.Page()
		if page == nil {
			c.Next()
			return
		}

		ua := RandomUserAgent()
		_ = page.SetUserAgent(ua)

		c.Next()
	}
}
